package com.BikeRental.VoltRide.model;

public class Vehicle {

    private int id;
    private String name;

    public Vehicle() {
    }

    public Vehicle(int id, String name) {
        this.id = id;
        this.name = name;
    }

    // Getters & Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
